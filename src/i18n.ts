function localeStringField(
  value: unknown,
  field: string,
  maximumLength: number,
): string {
  if (
    typeof value !== "string" ||
    value.trim().length === 0 ||
    value.length > maximumLength
  ) {
    throw new Error(`Locale ${field} is invalid.`);
  }
  return value.trim();
}

function parseContributedLocale(value: unknown): ContributedLocale {
  if (typeof value !== "object" || value === null || Array.isArray(value)) {
    throw new Error("Locale must be a JSON object.");
  }
  const candidate = value as Record<string, unknown>;
  if (candidate.schemaVersion !== 1) {
    throw new Error("This locale schema version is not supported.");
  }
  const rawLocale = localeStringField(candidate.locale, "code", 35);
  let locale: string;
  try {
    locale = Intl.getCanonicalLocales(rawLocale)[0] ?? "";
  } catch {
    throw new Error("Locale code is invalid.");
  }
  if (
    locale.length === 0 ||
    ["system", "en", "ja", "es"].includes(locale.toLocaleLowerCase())
  ) {
    throw new Error("Locale code is reserved.");
  }
  const translations = candidate.translations;
  if (
    typeof translations !== "object" ||
    translations === null ||
    Array.isArray(translations)
  ) {
    throw new Error("Locale translations must be a JSON object.");
  }
  const entries = Object.entries(translations);
  if (entries.length === 0 || entries.length > 2_000) {
    throw new Error("Locale must contain between 1 and 2,000 translations.");
  }
  const checkedTranslations: Record<string, string> = {};
  for (const [key, translation] of entries) {
    if (
      key.length === 0 ||
      key.length > 240 ||
      typeof translation !== "string" ||
      translation.length > 2_000
    ) {
      throw new Error("Locale contains an invalid translation.");
    }
    checkedTranslations[key] = translation;
  }
  return {
    schemaVersion: 1,
    locale,
    name: localeStringField(candidate.name, "name", 80),
    nativeName: localeStringField(candidate.nativeName, "native name", 80),
    translations: checkedTranslations,
  };
}

const contributedLocales = Object.entries(contributedLocaleModules).flatMap(
  ([source, value]) => {
    try {
      return [parseContributedLocale(value)];
    } catch (error) {
      console.error(`Koma ignored invalid bundled locale ${source}:`, error);
      return [];
    }
  },
);

const availableLanguages: AvailableLanguage[] = [
  { locale: "en", name: "English", nativeName: "English" },
  { locale: "ja", name: "Japanese", nativeName: "日本語" },
  { locale: "es", name: "Spanish", nativeName: "Español" },
  ...contributedLocales
    .map(({ locale, name, nativeName }) => ({ locale, name, nativeName }))
    .sort((left, right) => left.nativeName.localeCompare(right.nativeName)),
];

function availableLocale(language: string): string | undefined {
  return availableLanguages.find(
    (candidate) =>
      candidate.locale.toLocaleLowerCase() === language.toLocaleLowerCase(),
  )?.locale;
}

export function isLanguageAvailable(language: string): boolean {
  return availableLocale(language) !== undefined;
}

export function listAvailableLanguages(): AvailableLanguage[] {
  return availableLanguages.map((language) => ({ ...language }));
}

function bestSupportedLanguage(): string {
  const preferred =
    typeof navigator === "undefined" ? ["en"] : navigator.languages;
  for (const candidate of preferred) {
    let canonical = candidate;
    try {
      canonical = Intl.getCanonicalLocales(candidate)[0] ?? candidate;
    } catch {
      continue;
    }
    const exact = availableLocale(canonical);
    if (exact !== undefined) return exact;
    const base = canonical.toLocaleLowerCase().split("-")[0];
    const related = availableLanguages.find(
      (language) => language.locale.toLocaleLowerCase().split("-")[0] === base,
    )?.locale;
    if (related !== undefined) return related;
  }
  return "en";
}

export function resolveLanguage(mode: LanguageMode): string {
  if (mode === "system") return bestSupportedLanguage();
  return availableLocale(mode) ?? "en";
}

const savedLanguage = localStorage.getItem("koma.language");
const initialMode: LanguageMode =
  savedLanguage !== null && isLanguageAvailable(savedLanguage)
    ? savedLanguage
    : "system";

const resources: Record<string, { translation: Record<string, string> }> = {
  en: { translation: {} },
  ja: { translation: { ...translations.ja } },
  es: { translation: { ...translations.es } },
};
for (const language of contributedLocales) {
  resources[language.locale] = { translation: language.translations };
}

void i18next.use(initReactI18next).init({
  lng: resolveLanguage(initialMode),
  fallbackLng: "en",
  keySeparator: false,
  interpolation: { escapeValue: false },
  resources,
});

export async function applyLanguage(mode: LanguageMode): Promise<void> {
  const resolved = resolveLanguage(mode);
  document.documentElement.lang = resolved;
  await i18next.changeLanguage(resolved);
}

export function locale(): string {
  return i18next.resolvedLanguage ?? i18next.language ?? "en";
}

export function tr(
  key: string,
  options?: Record<string, string | number | boolean | null | undefined>,
): string {
  return String(options === undefined ? i18next.t(key) : i18next.t(key, options));
}

